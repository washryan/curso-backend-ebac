public class CalculoMedia {
    public static void main(String[] args) {
        double nota1 = 8.5;
        double nota2 = 7.0;
        double nota3 = 9.0;
        double nota4 = 6.5;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("Media das notas: " + media);
    }
}
